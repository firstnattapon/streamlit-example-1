    st.write(f"üß¨ Evolving Solutions using GA (Master Seed: {master_seed})")
    st.write(f"üìä ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•: {n} ‡∏ß‡∏±‡∏ô | Window: {window_size} ‡∏ß‡∏±‡∏ô | ‡∏õ‡∏£‡∏∞‡∏ä‡∏≤‡∏Å‡∏£: {population_size} | ‡∏£‡∏∏‡πà‡∏ô: {generations}"); st.write("---")

