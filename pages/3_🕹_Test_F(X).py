@njit(cache=True)
def _sigmoid(x: np.ndarray) -> np.ndarray:
    """ ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô Sigmoid ‡∏ó‡∏µ‡πà‡πÄ‡∏£‡πà‡∏á‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏£‡πá‡∏ß‡∏î‡πâ‡∏ß‡∏¢ Numba """
    return 1.0 / (1.0 + np.exp(-x))

def find_best_solution_gradient_descent(prices_window: np.ndarray, iterations: int, learning_rate: float, seed: int) -> Tuple[float, np.ndarray]:
    """
    ‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤ Action Sequence ‡∏ó‡∏µ‡πà‡∏î‡∏µ‡∏ó‡∏µ‡πà‡∏™‡∏∏‡∏î‡πÇ‡∏î‡∏¢‡πÉ‡∏ä‡πâ Gradient Descent
    - ‡∏™‡∏£‡πâ‡∏≤‡∏á Objective Function ‡∏ó‡∏µ‡πà Differentiable ‡πÑ‡∏î‡πâ ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡∏´‡∏≤ Gradient ‡πÑ‡∏î‡πâ
    - Objective: ‡∏û‡∏¢‡∏≤‡∏¢‡∏≤‡∏°‡∏™‡∏£‡πâ‡∏≤‡∏á Action ‡πÉ‡∏´‡πâ‡∏™‡∏≠‡∏î‡∏Ñ‡∏•‡πâ‡∏≠‡∏á‡∏Å‡∏±‡∏ö‡∏Å‡∏≤‡∏£‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡πÅ‡∏õ‡∏•‡∏á‡∏Ç‡∏≠‡∏á‡∏£‡∏≤‡∏Ñ‡∏≤‡πÉ‡∏ô‡∏ß‡∏±‡∏ô‡∏ñ‡∏±‡∏î‡πÑ‡∏õ (‡∏ã‡∏∑‡πâ‡∏≠‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏Ñ‡∏≤‡∏î‡∏ß‡πà‡∏≤‡∏£‡∏≤‡∏Ñ‡∏≤‡∏à‡∏∞‡∏Ç‡∏∂‡πâ‡∏ô)
    - ‡∏õ‡∏£‡∏±‡∏ö‡∏Ñ‡πà‡∏≤‡∏û‡∏≤‡∏£‡∏≤‡∏°‡∏¥‡πÄ‡∏ï‡∏≠‡∏£‡πå `theta` ‡∏î‡πâ‡∏ß‡∏¢ Gradient Descent ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏•‡∏î Loss
    - ‡πÅ‡∏õ‡∏•‡∏á `theta` ‡∏ó‡∏µ‡πà‡πÑ‡∏î‡πâ‡∏ú‡∏•‡∏î‡∏µ‡∏ó‡∏µ‡πà‡∏™‡∏∏‡∏î‡πÄ‡∏õ‡πá‡∏ô Binary Actions (0 ‡∏´‡∏£‡∏∑‡∏≠ 1)
    """
    window_len = len(prices_window)
    if window_len < 2: return 0.0, np.ones(window_len, dtype=np.int32)
    
    # ‡πÉ‡∏ä‡πâ PCG64 ‡∏ã‡∏∂‡πà‡∏á‡πÄ‡∏õ‡πá‡∏ô Generator ‡∏ó‡∏µ‡πà‡∏ó‡∏±‡∏ô‡∏™‡∏°‡∏±‡∏¢‡πÅ‡∏•‡∏∞‡∏°‡∏µ‡∏Ñ‡∏∏‡∏ì‡∏™‡∏°‡∏ö‡∏±‡∏ï‡∏¥‡∏ó‡∏≤‡∏á‡∏™‡∏ñ‡∏¥‡∏ï‡∏¥‡∏ó‡∏µ‡πà‡∏î‡∏µ
    rng = np.random.default_rng(np.random.PCG64(seed))
    # theta ‡∏Ñ‡∏∑‡∏≠‡∏û‡∏≤‡∏£‡∏≤‡∏°‡∏¥‡πÄ‡∏ï‡∏≠‡∏£‡πå‡∏ó‡∏µ‡πà‡πÄ‡∏£‡∏≤‡∏à‡∏∞‡∏ó‡∏≥‡∏Å‡∏≤‡∏£ Optimize
    theta = rng.standard_normal(size=window_len, dtype=np.float64)

    # ‡∏™‡∏±‡∏ç‡∏ç‡∏≤‡∏ì‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏Å‡∏≤‡∏£‡πÄ‡∏£‡∏µ‡∏¢‡∏ô‡∏£‡∏π‡πâ: ‡∏Å‡∏≤‡∏£‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡πÅ‡∏õ‡∏•‡∏á‡∏Ç‡∏≠‡∏á‡∏£‡∏≤‡∏Ñ‡∏≤ (p_{i+1} - p_i)
    # ‡πÄ‡∏£‡∏≤‡πÉ‡∏ä‡πâ np.diff ‡πÅ‡∏•‡∏∞‡πÄ‡∏ï‡∏¥‡∏°‡∏Ñ‡πà‡∏≤‡∏™‡∏∏‡∏î‡∏ó‡πâ‡∏≤‡∏¢‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡∏°‡∏µ‡∏Ç‡∏ô‡∏≤‡∏î‡πÄ‡∏ó‡πà‡∏≤‡∏Å‡∏±‡∏ö window_len
    price_changes = np.diff(prices_window, append=prices_window[-1])
    
    # Optimization Loop
    for _ in range(iterations):
        soft_actions = _sigmoid(theta) # ‡∏Ñ‡πà‡∏≤ Action ‡πÅ‡∏ö‡∏ö‡∏ï‡πà‡∏≠‡πÄ‡∏ô‡∏∑‡πà‡∏≠‡∏á‡∏£‡∏∞‡∏´‡∏ß‡πà‡∏≤‡∏á 0-1
        
        # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì Gradient ‡∏Ç‡∏≠‡∏á Loss Function ‡πÄ‡∏ó‡∏µ‡∏¢‡∏ö‡∏Å‡∏±‡∏ö theta
        # Loss = sum(-soft_action * price_change) -> ‡∏û‡∏¢‡∏≤‡∏¢‡∏≤‡∏°‡∏ó‡∏≥‡πÉ‡∏´‡πâ soft_action ‡∏™‡∏π‡∏á‡πÄ‡∏°‡∏∑‡πà‡∏≠ price_change ‡πÄ‡∏õ‡πá‡∏ô‡∏ö‡∏ß‡∏Å
        # Gradient = -price_change * sigmoid_derivative
        gradient = -price_changes * soft_actions * (1.0 - soft_actions)
        
        # ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏û‡∏≤‡∏£‡∏≤‡∏°‡∏¥‡πÄ‡∏ï‡∏≠‡∏£‡πå theta
        theta -= learning_rate * gradient

    # ‡πÅ‡∏õ‡∏•‡∏á theta ‡∏ó‡∏µ‡πà Optimize ‡πÅ‡∏•‡πâ‡∏ß‡πÄ‡∏õ‡πá‡∏ô Binary Actions
    final_actions = (_sigmoid(theta) > 0.5).astype(np.int32)
    final_actions[0] = 1 # ‡∏ß‡∏±‡∏ô‡πÅ‡∏£‡∏Å‡∏ï‡πâ‡∏≠‡∏á‡∏ã‡∏∑‡πâ‡∏≠‡πÄ‡∏™‡∏°‡∏≠

    # ‡∏õ‡∏£‡∏∞‡πÄ‡∏°‡∏¥‡∏ô‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡∏™‡∏∏‡∏î‡∏ó‡πâ‡∏≤‡∏¢‡∏î‡πâ‡∏ß‡∏¢ Simulation ‡∏à‡∏£‡∏¥‡∏á
    _, sumusd, _, _, _, refer = calculate_optimized_cached(tuple(final_actions), tuple(prices_window))
    net = sumusd[-1] - refer[-1] - sumusd[0] if len(sumusd) > 0 else -np.inf
        
    return net, final_actions

def generate_actions_sliding_window_gd(ticker_data: pd.DataFrame, window_size: int, iterations: int, learning_rate: float, master_seed: int) -> Tuple[np.ndarray, pd.DataFrame]:
    """ ‡∏™‡∏£‡πâ‡∏≤‡∏á Action Sequence ‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡πÇ‡∏î‡∏¢‡πÉ‡∏ä‡πâ‡∏Å‡∏•‡∏¢‡∏∏‡∏ó‡∏ò‡πå Gradient Descent Optimizer """
    prices = ticker_data['Close'].to_numpy(); n = len(prices)
    final_actions = np.array([], dtype=np.int32); window_details_list = []
    num_windows = (n + window_size - 1) // window_size
    progress_bar = st.progress(0, text="‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏• Gradient Descent Optimizer...")

    for i, start_index in enumerate(range(0, n, window_size)):
        end_index = min(start_index + window_size, n)
        prices_window = prices[start_index:end_index]
        window_len = len(prices_window)
        if window_len < 2: continue
        
        window_seed = master_seed + i # ‡πÉ‡∏ä‡πâ Seed ‡∏ó‡∏µ‡πà‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡πÑ‡∏õ‡πÉ‡∏ô‡πÅ‡∏ï‡πà‡∏•‡∏∞ Window ‡πÅ‡∏ï‡πà‡∏¢‡∏±‡∏á‡∏Ñ‡∏á‡πÄ‡∏î‡∏¥‡∏°‡∏ó‡∏∏‡∏Å‡∏Ñ‡∏£‡∏±‡πâ‡∏á‡∏ó‡∏µ‡πà‡∏£‡∏±‡∏ô
        max_net, best_actions = find_best_solution_gradient_descent(prices_window, iterations, learning_rate, seed=window_seed)
        
        final_actions = np.concatenate((final_actions, best_actions))
        
        start_date_str = ticker_data.index[start_index].strftime('%Y-%m-%d'); end_date_str = ticker_data.index[end_index-1].strftime('%Y-%m-%d')
        detail = {
            'window_number': i + 1, 'timeline': f"{start_date_str} ‡∏ñ‡∏∂‡∏á {end_date_str}",
            'max_net': round(max_net, 2), 'action_count': int(np.sum(best_actions)),
            'window_size': window_len, 'window_seed': window_seed
        }
        window_details_list.append(detail)
        progress_bar.progress((i + 1) / num_windows, text=f"Optimizing Window {i+1}/{num_windows}")
        
    progress_bar.empty()
    return final_actions, pd.DataFrame(window_details_list)

